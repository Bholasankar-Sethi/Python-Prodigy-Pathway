"""When denominator is +ve
remainder is always +ve"""
a, b = -16, 5   # denominator +ve
print(a % b)

x, y = 16, 5
print(x % y)


A, B = 16, -5      # denominator is -ve
C = A % B
print(C)

x, y = -16, -5
print(x % y)

